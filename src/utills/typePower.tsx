export const typePower = (Background: string) => {
  switch (Background) {
    case "fire":
      return "#CF2C03";
    case "water":
      return "#3B9BF1";
    case "grass":
      return "#67AF32";
    case "bug":
      return "#87950C";
    case "dragon":
      return "#7361D1";
    case "electric":
      return "#E08D00";
    case "dark":
      return "#413831";
    case "flying":
      return "#8FA4FF";
    case "normal":
      return "#C4C0B4";
    case "fighting":
      return "#852816";
    case "rock":
      return "#5E491C";
    case "ice":
      return "#7F8488";
    case "steel":
      return "#474747";
    case "poison":
      return "#924990";
    case "ghost":
      return "#6969AF";
    case "psychic":
      return "#E25484";
    case "ground":
      return "#C5A455";
    case "fairy":
      return "#E29FE6";
    default:
  }
};
